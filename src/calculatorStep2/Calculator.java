package calculatorStep2;

public class Calculator {

    int sum(int a, int b) {
        int result = a + b;
        return result;
    }

    int sub(int a, int b) {
        int result = a - b;
        return result;
    }

    int mul(int a, int b) {
        int result = a * b;
        return result;
    }

    int div(int a, int b) {
        int result = a / b;
        return result;
    }
}

